# Crie um algoritmo que leia um número e mostre o seu dobro,
# triplo e o seu quadrado.

n1 = int(input("digite um numero: "))
print(f"valor digitado é {n1}")
print(f"o dobro de {n1} é {n1*2}")
print(f"o triplo de {n1} é {n1*3}")
print(f"o valor de {n1} ao quadrado é {n1**n1}")